self.get = async(req, res) => {
    try {
        let id = req.params.id;
        let data = await employeeage.findOne({
            where: {
                id: id
            }
        });
        return res.status(200).json({
            data: (data) ? data : {}
        })
    } catch (error) {
        res.status(500).json({
            message: error.message
        })
    }
}
